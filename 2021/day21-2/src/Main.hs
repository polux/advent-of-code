main :: IO ()
main = print solution

draws = cycle [1 .. 100]

move i d = ((i + d -1) `mod` 10) + 1

solution =
  let (Sum s1, Sum s2) = count (4, 0) (2, 0)
  in max s1 s2

memoCount = memo2 count

count :: (Int, Int) -> (Int, Int) -> (Sum Int, Sum Int)
count (p1Pos, p1Score) (p2Pos, p2Score)
  | p2Score >= 21 = (Sum 1, Sum 0)
  | otherwise = mconcat $ do
      d1 <- [1,2,3]
      d2 <- [1,2,3]
      d3 <- [1,2,3]
      let newP1Pos = move p1Pos (d1 + d2 + d3)
      return $ swap $ memoCount (p2Pos, p2Score) (newP1Pos, p1Score + newP1Pos)