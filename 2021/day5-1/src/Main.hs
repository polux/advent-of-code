type Input = [(V2 Int, V2 Int)]

type Output = Int

main :: IO ()
main = readFile "input" >>= print . solve . parse

parse :: String -> Input
parse = map parseLine . lines
 where
   parseLine (words->[p1, "->", p2]) = (parsePoint p1, parsePoint p2)
   parsePoint (splitOn "," -> [n1, n2]) = V2 (read n1) (read n2)

segmentToMap :: (V2 Int, V2 Int) -> Map (V2 Int) Int
segmentToMap s@(V2 x1 y1, V2 x2 y2)
  | y1 == y2 = M.fromList [(V2 x y1,1) | x <- [min x1 x2..max x1 x2]]
  | x1 == x2 = M.fromList [(V2 x1 y,1) | y <- [min y1 y2..max y1 y2]]
  | otherwise = mempty


solve :: Input -> Output
solve input =
  M.unionsWith (+) (map segmentToMap input)
  & M.filter (>1)
  & length
