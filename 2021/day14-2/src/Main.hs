type Input = (String, [(Char, Char, Char)])

type Rules = Map (Char, Char) Char

type Output = Int

main :: IO ()
main = readFile "input" >>= print . solve . parse

parse :: String -> Input
parse (lines->(template:_:rules)) = (template, map parseRule rules)
 where
   parseRule [c1,c2,_,_,_,_,c3] = (c1, c2, c3)

mkRules rules = M.fromList [((a,b), c) | (a,b,c) <- rules]

solve :: Input -> Output
solve (start, mkRules->rules) = score (totalCount 40 start)
  where
    totalCount n str@(c:cs) = M.unionsWith (+) (M.singleton c 1 : zipWith (memoCount n) str cs)

    memoCount = memo3 count
    count :: Int -> Char -> Char -> Map Char Int
    count 0 a b = M.singleton b 1
    count n a b = let c = rules M.! (a,b)
                  in M.unionWith (+) (memoCount (n-1) a c) (memoCount (n-1) c b)

    score counts = maximum counts - minimum counts
