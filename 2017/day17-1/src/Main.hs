input = 359

main :: IO ()
main = solve >>= print

jump :: MUV.IOVector Int -> Int -> Int -> IO Int
jump vec i 0 = return i
jump vec i n = do
  j <- MUV.read vec i
  jump vec j (n-1)

solve = do
  vec <- MUV.replicate 2018 (0::Int)
  go vec 0
 where
  go vec 2017 = MUV.read vec 2017
  go vec i = do
    j <- jump vec i input
    jNext <- MUV.read vec j
    MUV.write vec j (i+1)
    MUV.write vec (i+1) jNext
    go vec (i+1)
