type Input = [(String, Map String Int)]

type Output = [String]

message =
  M.fromList
    [ ("children", 3),
      ("cats", 7),
      ("samoyeds", 2),
      ("pomeranians", 3),
      ("akitas", 0),
      ("vizslas", 0),
      ("goldfish", 5),
      ("trees", 3),
      ("cars", 2),
      ("perfumes", 1)
    ]

main :: IO ()
main = getContents >>= print . solve . parse

parse :: String -> Input
parse = map parseLine . lines
 where
  parseLine str =
   let (name, props) = span (/=':') str
    in (name, props & drop 2 & splitOn ", " & map (splitOn ": ") & map parseEntry & M.fromList)
  parseEntry [name, num] = (name, read num)
  parseEntry _ = error "unexpected entry"

compatible :: Map String Int -> Map String Int -> Bool
compatible m1 m2 = and $ M.elems $ M.intersectionWith (==) m1 m2

solve :: Input -> Output
solve input = input & filter (compatible message . snd) & map fst
