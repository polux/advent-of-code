type Input = [Int]

main :: IO ()
main = readFile "input" >>= print . solve . parse

parse :: String -> Input
parse = map (read . singleton) . head . lines

solve input = sum $ zipWith (*) (defrag (expand 0 input)) [0..]
 where
  expand i [] = []
  expand i [n] = replicate n (Just i)
  expand i (n : m : ns) = replicate n (Just i) <> replicate m Nothing <> expand (i + 1) ns

  defrag s = go 0 (length s - 1) s (reverse s)
   where
    go a b _ _ | a > b = []
    go a b [] _ = []
    go a b ns (Nothing : tail) = go a (b-1) ns tail
    go a b (Nothing : tail) (Just n : ns) = n : go (a+1) (b-1) tail ns
    go a b (Just c : tail) ns = c : go (a+1) b tail ns
    go _ _ _ _ = error "!"
