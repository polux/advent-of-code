type Input = UA.Array (V2 Int) Char

main :: IO ()
main = readFile "input" >>= print . solve . parse

parse :: String -> Input
parse = arrayFromList2D . lines

solve input = filter good (pairs distances) & length
 where
  n2d = neighbors2D (arraySize input)
  at p = input UA.! p
  isWall p = at p == '#'

  start = head [p | (p, c) <- UA.assocs input, c == 'S']
  end = head [p | (p, c) <- UA.assocs input, c == 'E']
  distances = bfs (filter (not . isWall) . n2d) (== end) start & fromJust & (`zip` [0 ..])

  pairs xs = [(x,y) | x:xs' <- tails xs, y <- xs']

  good ((i,id), (o,od)) = canReach i o &&  (od-id)-sum (fmap abs (o-i)) >= 100
  canReach p0 p1 = sum (fmap abs (p1-p0)) <= 20