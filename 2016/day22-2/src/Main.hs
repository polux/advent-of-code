type Coord = V2 Int
data Node = Node { coord :: Coord, size :: Int, used :: Int }
  deriving (Eq, Show, Generic)

type Input = [Node]


main :: IO ()
main = getContents >>= putStrLn . solve . parse

parse :: String -> Input
parse = map parseLine . drop 2 . lines
 where
   parseLine str =
     let [[_, x, y, size, used]] = str =~ "/dev/grid/node-x(\\d+)-y(\\d+)\\s+(\\d+)T\\s+(\\d+)T"
     in Node (V2 (read x) (read y)) (read size) (read used)

display :: [Node] -> String
display nodes = unlines [prettyLine y | y <- [0..yMax]]
 where
   xMax = fromJust $ maximumOf (folded . #coord . _x) nodes
   yMax = fromJust $ maximumOf (folded . #coord . _y) nodes
   prettyLine y = unwords [prettyCell (valueAt x y) | x <- [0..xMax]]
   valueAt x y = head $ filter ((==V2 x y).coord) nodes
   sizeOf0 = size $ head $ filter ((==0) . used) nodes
   prettyCell (Node _ _ used)
     | used == 0 = "_"
     | used <= sizeOf0 = "."
     | otherwise = "#"

solve input = display input

