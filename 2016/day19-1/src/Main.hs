solve :: Int -> IO Int
solve len = do
  vec <- MUV.unsafeNew len
  forM_ [0..len-2] $ \i -> do
    MUV.write vec i (i+1)
  MUV.write vec (len-1) 0
  res <- go 0 vec
  return (res+1)
 where
  go :: Int -> MUV.IOVector Int -> IO Int
  go i vec = do
    next <- MUV.read vec i
    if next == i
      then return i
      else do
        nextNext <- MUV.read vec next
        MUV.write vec i nextNext
        go nextNext vec